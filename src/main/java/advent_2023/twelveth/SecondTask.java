    private static Long check(String pattern,
                              ArrayList<Long> numbers,
                              int matchedGroupsIndex,
                              long currentGroupLength,
                              int position) {
        var parameters = new Parameters(matchedGroupsIndex, currentGroupLength, position);
        if(resultMemory.containsKey(parameters)) {
            return resultMemory.get(parameters);
        }

        if(position >= pattern.length()) {
            return checkEnding(numbers, matchedGroupsIndex, currentGroupLength, parameters);
        }

        var currentPattern = pattern.charAt(position);

        if(currentPattern == '.') {
            return checkDot(pattern, numbers, matchedGroupsIndex, currentGroupLength, position, parameters);
        }

        if(currentPattern == '#') {
            return checkHash(pattern, numbers, matchedGroupsIndex, currentGroupLength, position, parameters);
        }

        if(currentPattern == '?') {
            return checkDot(pattern, numbers, matchedGroupsIndex, currentGroupLength, position, parameters)
                    + checkHash(pattern, numbers, matchedGroupsIndex, currentGroupLength, position, parameters);
        }

        throw new RuntimeException("Unknown pattern: " + currentPattern);
    }

    private static long checkEnding(ArrayList<Long> numbers, int matchedGroupsIndex, long currentGroupLength, Parameters parameters) {
        if(numbers.size() == matchedGroupsIndex && currentGroupLength == 0) {
            resultMemory.put(parameters, 1L);
            return 1L;
        }

        if(numbers.size() - 1 == matchedGroupsIndex && currentGroupLength != 0) {
            if(currentGroupLength == numbers.get(matchedGroupsIndex)) {
                resultMemory.put(parameters, 1L);
                return 1L;
            } else {
                resultMemory.put(parameters, 0L);
                return 0L;
            }
        }

        resultMemory.put(parameters, 0L);
        return 0L;
    }

    private static Long checkHash(String pattern, ArrayList<Long> numbers, int matchedGroupsIndex, long currentGroupLength, int position, Parameters parameters) {
        if(matchedGroupsIndex >= numbers.size()) {
            resultMemory.put(parameters, 0L);
            return 0L;
        }

        if(currentGroupLength + 1 > numbers.get(matchedGroupsIndex)) {
            resultMemory.put(parameters, 0L);
            return 0L;
        }

        var newParameters = new Parameters(
                matchedGroupsIndex,
                currentGroupLength + 1,
                position + 1
        );
        var result = check(pattern, numbers, matchedGroupsIndex, currentGroupLength + 1, position + 1);
        resultMemory.put(newParameters, result);
        return result;
    }

    private static Long checkDot(String pattern, ArrayList<Long> numbers, int matchedGroupsIndex, long currentGroupLength, int position, Parameters parameters) {
        if(currentGroupLength > 0) {
            long currentGroupExpectedLength = numbers.get(matchedGroupsIndex);
            if(currentGroupLength == currentGroupExpectedLength) {
                var newParameters = new Parameters(matchedGroupsIndex + 1, 0, position + 1);
                var result = check(pattern, numbers, matchedGroupsIndex + 1, 0, position + 1);
                resultMemory.put(newParameters, result);
                return result;
            } else {
                resultMemory.put(parameters, 0L);
                return 0L;
            }
        } else {
            var newParameters = new Parameters(matchedGroupsIndex, 0, position + 1);
            var result = check(pattern, numbers, matchedGroupsIndex, 0, position + 1);
            resultMemory.put(newParameters, result);
            return result;
        }
    }
